using namespace std;

int main()
{

    int n;
    cin >> n;
    int x = 0;
    string ex[151];
    for (int i = 0; i < n; i++)
    {
        cin >> ex[i];
    }

    for (size_t i = 0; i < n; i++)
    {
        string s = ex[i];
        if ((s[0] == '+') || (s[2] == '+'))
        {
            x = x + 1;
        }
        else if ((s[0] == '-') || (s[2] == '-'))
        {
            x = x - 1;
        }
    }
    cout << x;

    return 0;
}