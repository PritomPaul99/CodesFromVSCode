using namespace std;

int main()
{
    int n;
    cin >> n;
    int arr1[10], arr2[10000000000];

    for (int i = 0; i < n; i++)
    {
        cin >> arr1[i];
        arr2[arr1[i]]++;
    }

    cout << "Array1:-" << endl;
    for (int i = 0; i < n; i++)
    {
        cout << arr1[i] << " ";
    }
    cout << endl;

    cout << "Array2:-" << endl;
    for (int i = 0; i < n; i++)
    {
        cout << arr2[i] << " ";
    }
    cout << endl;

    return 0;
}

