#include <bits/stdc++.h> 
using namespace std;
int main()
{
    int n;
    string s;
    int A = 0, D = 0;
    cin >> n;
    cin >> s;

    for (size_t i = 0; i < s.size(); i++)
    {
        if (s[i] == 'A')
        {
            A++;
        }
        else if (s[i] == 'D')
        {
            D++;
        }
    }
    if (A == D)
    {
        cout << "Friendship";
    }
    else if (A > D)
    {
        cout << "Anton";
    }
    else
    {
        cout << "Danik";
    }
}