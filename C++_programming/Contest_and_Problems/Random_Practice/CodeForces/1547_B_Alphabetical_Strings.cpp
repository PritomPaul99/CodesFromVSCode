void solve()
{
    string s;
    cin >> s;
    int n, c(0), i = 0, j = n = s.size();

    while (n--)
    {
        if (s[i] == 'a' + n)
        {
            i++;
        }
        else if (s[j - 1] == 'a' + n)
        {
            j--;
        }
        else
        {
            c = 1;
            break;
        }
    }
    c == 0 ? cout << "Yes" << endl : cout << "No" << endl;
}

