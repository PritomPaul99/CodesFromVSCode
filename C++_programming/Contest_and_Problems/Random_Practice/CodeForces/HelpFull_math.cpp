#include <bits/stdc++.h>
using namespace std;
int main()
{
    char s[101];
    cin >> s;

    for (int i = 0; i < strlen(s); i += 2)
    {
        for (int j = i + 2; j < strlen(s); j += 2)
        {
            if (s[i] > s[j])
            {
                swap(s[i], s[j]);
            }
        }
    }
    cout << s;
}