#include <bits/stdc++.h>
using namespace std;
int main()
{
    char s[101];

    cin >> s;
    int up = 0, low = 0;

    //cout << strlen(s) << endl;

    for (int i = 0; i < strlen(s); i++)
    {
        if (s[i] >= 97 && s[i] <= 122)
        {
            low++;
        }
        else
        {
            up++;
        }
    }

    if (up > low)
    {
        for (int i = 0; i < strlen(s); i++)
        {
            cout << (char)toupper(s[i]);
        }
    }
    else
    {
        for (int i = 0; i < strlen(s); i++)
        {
            cout << (char)tolower(s[i]);
        }
    }
}