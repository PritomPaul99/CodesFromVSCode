#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cin >> n;
    int enter, exit, c1 = 0, c2 = 0;

    while (n--)
    {
        cin >> exit >> enter;

        c1 = c1 + (enter - exit);
        if (c1 > c2)
        {
            c2 = c1;
        }
    }
    cout << c2;
}