using namespace std;

void solve()
{
    int n;
    cin >> n;

    char str[n];
    cin >> str;

    set<char> ch;
    for (int i = 0; i < n; i++)
    {
        str[i] = (char)tolower(str[i]);
        if (str[i] >= 'a' && str[i] <= 'z')
        {
            ch.insert(str[i]);
        }
    }
    if (ch.size() == 26)
    {
        cout << "Yes";
    }
    else
    {
        cout << "No";
    }
}

