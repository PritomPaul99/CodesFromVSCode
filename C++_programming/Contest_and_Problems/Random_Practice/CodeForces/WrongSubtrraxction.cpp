#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n, k, rem, i = 0;
    cin >> n >> k;

    while (k--)
    {
        rem = n % 10;

        if (rem == 0)
        {
            n = n / 10;
        }
        else
        {
            n = n - 1;
        }
        i++;
    }
    cout << n;
}