#include <bits/stdc++.h>
using namespace std;
int main()
{
    int mat[5][5];
    int fori, forj;

    for (int i = 0; i < 5; i++)
    {
        for (int j = 0; j < 5; j++)
        {
            cin >> mat[i][j];

            if (mat[i][j] == 1)
            {
                fori = i;
                forj = j;
            }
        }
    }

    int n = abs(2 - fori) + abs(2 - forj);

    cout << n;
}