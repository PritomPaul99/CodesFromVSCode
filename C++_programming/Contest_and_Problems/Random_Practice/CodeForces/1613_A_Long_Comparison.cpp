void solve()
{
    int x1, p1, x2, p2;
    cin >> x1 >> p1 >> x2 >> p2;
    int m = min(p1, p2);
    p1 -= m, p2 -= m;

    if (p1 >= 7)
    {
        cout << '>' << endl;
        return;
    }
    else if (p2 >= 7)
    {
        cout << '<' << endl;
        return;
    }
    auto a = x1 * pow(10, p1);
    auto b = x2 * pow(10, p2);

    if (a > b)
    {
        cout << ">" << endl;
    }
    else if (a < b)
    {
        cout << "<" << endl;
    }
    else
    {
        cout << "=" << endl;
    }
}

