#include <bits/stdc++.h>
using namespace std;
int main()
{
    long long n, x;
    int count = 0;
    cin >> n;

    while (n != 0)
    {
        x = n % 10;
        n = n / 10;

        if(x == 7 || x == 4)
        {
            count++;
        }
    }


    if (count == 7 || count == 4)
    {
        cout << "Yes";
    }
    else
    {
        cout << "NO";
    }
}