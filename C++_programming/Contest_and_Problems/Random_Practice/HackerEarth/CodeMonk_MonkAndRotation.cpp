void rotation(int *arr, int n)
{
    int i = n - 1;
    int temp = arr[n - 1];
    while (i > 0)
    {
        arr[i] = arr[i - 1];
        i--;
    }
    arr[0] = temp;
    return;
}

void solve()
{
    int n, k;
    cin >> n >> k;

    int arr[n];
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    while (k--)
    {
        rotation(arr, n);
    }

    for (int i = 0; i < n; i++)
    {
        cout << arr[i] << " ";
    }
}

