#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        int a[100000];
        long long count = 1;
        int result;

        for (int i = 0; i < n; i++)
        {
            cin >> a[i];
            count = count * a[i];
        }

        result = sqrt(count);
        if ((result * result) == count)
        {
            cout << "NO" << endl;
        }
        else
        {
            cout << "YES" << endl;
        }
    }
}