void solve()
{
    ll n, k;
    cin >> n >> k;

    ll arr[n];
    for (int i = 1; i <= n; i++)
    {
        cin >> arr[i];
    }
    ll m = -99999999999;
    for (int i = 1; i < n; i++)
    {
        for (int j = i + 1; j <= n; j++)
        {
            ll x = (i * j) - (k * (arr[i] | arr[j]));
            if(x>m)
            {
                m = x;
            }
        }
    }
    cout << m << endl;
}

