void solve()
{
    int a, b, c, m;
    cin >> a >> b >> c >> m;
    int sum = a + b + c;
    int mx = max({a, b, c});
    int mn = min({a, b, c});
    int mid = sum - (mx + mn);
    if (m == 0)
    {
        if (mid + mn >= mx)
        {
            cout << "Yes\n";
            return;
        }
        else
        {
            cout << "No\n";
            return;
        }
    }
    if (a % 2 != 0)
    {
        a = a - 1;
    }
    if (b % 2 != 0)
    {
        b = b - 1;
    }
    if (c % 2 != 0)
    {
        c = c - 1;
    }
    int sum2 = a + b + c;

    (sum2 >= m) ? cout << "Yes\n" : cout << "No\n";
}

