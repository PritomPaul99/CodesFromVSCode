#include <bits/stdc++.h>

#define f_input freopen("input.txt", "r", stdin)
#define f_output freopen("output.txt", "w", stdout)
#define nl '\n'
#define FastIO ios_base::sync_with_stdio(false), cin.tie(NULL)

using namespace std;

void solve()
{
    int n;
    cin >> n;

    vector<vector<string>> v(3);
    map<string, int> mp;

    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < n; j++)
        {
            string x;
            cin >> x;
            v[i].push_back(x);
            mp[x]++;
        }
    }

    int c[] = {0, 0, 0};

    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < n; j++)
        {
            // cout << v[i][j] << " ";

            if (mp[v[i][j]] == 1)
            {
                c[i] += 3;
            }
            else if (mp[v[i][j]] == 2)
            {
                c[i] += 1;
            }
            else
            {
                continue;
            }
        }
        // cout << nl;
    }

    cout << c[0] << " " << c[1] << " " << c[2] << nl;
}

int main()
{
    FastIO;
#ifndef ONLINE_JUDGE
    double start = clock();
    f_input;
    f_output;
#endif

    int ttt, ca = 1;
    cin >> ttt;
    while (ttt--)
    {
        // cout << "Case " << ca << ": ";
        solve();
        // ca++;
    }

#ifndef ONLINE_JUDGE
    double time = (clock() - start) / CLOCKS_PER_SEC;
    cerr << "Running Time : " << time << "\n";
#endif
    return 0;
}

