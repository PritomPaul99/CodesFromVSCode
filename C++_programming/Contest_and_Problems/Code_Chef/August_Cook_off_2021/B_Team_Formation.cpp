void solve()
{
    int n;
    cin >> n;
    string s;
    string t;
    cin >> s >> t;
    int p = 0, l = 0, m = 0;

    for (int i = 0; i < n; i++)
    {
        if (s[i] == '1' && t[i] == '1')
        {
            m++;
        }
    }

    sort(s.begin(), s.end());
    sort(all(t));

    for (int i = 0; i < n; i++)
    {
        if (s[i] == '1' && t[i] == '1')
        {
            p++;
        }
    }
    int mem = abs(p - m);

    if (m + mem > n / 2)
    {
        cout << n / 2 << endl;
    }
    else
    {
        cout << m + mem << endl;
    }
}

