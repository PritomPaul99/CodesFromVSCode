using namespace std;

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        ll l, gd = 0, bd = 0;
        string s;
        cin >> l;
        cin >> s;

        for (int i = 0; i < l; i++)
        {
            if (s[i] == '1')
            {
                gd++;
            }
            else
            {
                bd++;
            }
            if (gd >= bd)
            {
                cout << "YES" << endl;
                break;
            }
        }
        if (bd > gd)
        {
            cout << "NO" << endl;
        }
    }
}
