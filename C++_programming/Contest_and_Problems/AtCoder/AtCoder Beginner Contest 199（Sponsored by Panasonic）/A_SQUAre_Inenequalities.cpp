#include <bits/stdc++.h>
using namespace std;

int squr(int n)
{
    return n * n;
}
int main()
{
    int a, b, c;
    cin >> a >> b >> c;

    if ((squr(a) + squr(b)) < squr(c))
    {
        cout << "Yes" << endl;
    }
    else
    {
        cout << "No" << endl;
    }
}