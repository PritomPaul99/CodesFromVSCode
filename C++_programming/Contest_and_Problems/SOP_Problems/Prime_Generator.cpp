#include <iostream>
using namespace std;
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int x, y, i, j;
        cin >> x >> y;
        for (i = x + 1; i <= y; i++)
        {
            int value = 0;
            for (j = 2; j < i / 2; ++j)
            {
                if (i % j == 0)
                {
                    value = 1;
                    break;
                }
            }
            if (value == 0)
            {
                cout << i << endl;
            }
        }
    }
}